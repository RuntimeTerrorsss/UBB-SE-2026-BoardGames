        private async Task<(ConversationApiService Service, ConversationController Controller)> CreateTestSubject(IServiceScope scope)
        {
            var dbFactory = scope.ServiceProvider.GetRequiredService<IDbContextFactory<AppDbContext>>();
            var setupDb = dbFactory.CreateDbContext();

            // ConversationApiService and AccountRepository are tightly coupled to the DB
            var accountRepo = new AccountRepository(dbFactory);
            var conversationRepo = new ConversationRepository(setupDb);
            var service = new ConversationApiService(conversationRepo, accountRepo);
            var controller = new ConversationController(service);

            return (service, controller);
        }

        [Fact]
        public async Task SendMessage_WithValidData_WritesToDatabaseCorrectly()
        {
            // Arrange
            using var scope = _fixture.Factory.Services.CreateScope();
            var dbFactory = scope.ServiceProvider.GetRequiredService<IDbContextFactory<AppDbContext>>();
            using var db = dbFactory.CreateDbContext();

            var sender = await db.Users.FirstAsync(u => u.PamUserId == 200);
            var receiver = await db.Users.FirstAsync(u => u.PamUserId == 201);

            // Create a dedicated conversation for this test
            var conversation = new Conversation
            {
                Participants = new List<ConversationParticipant>
                {
                    new ConversationParticipant { UserId = sender.PamUserId },
                    new ConversationParticipant { UserId = receiver.PamUserId }
                }
            };
            db.Conversations.Add(conversation);
            await db.SaveChangesAsync();

            var (_, controller) = await CreateTestSubject(scope);

            var messageDto = new MessageDataTransferObject(
                Id: 0,
                ConversationId: conversation.ConversationId,
                SenderId: sender.PamUserId,
                ReceiverId: receiver.PamUserId,
                SentAt: DateTime.UtcNow,
                Content: "Hello World!",
                Type: MessageType.MessageText,
                ImageUrl: string.Empty,
                IsResolved: false,
                IsAccepted: false,
                IsAcceptedByBuyer: false,
                IsAcceptedBySeller: false,
                RequestId: -1,
                PaymentId: -1,
                RentalId: -1
            );

            // Act
            var result = await controller.SendMessage(messageDto);

            // Assert
            var okResult = Assert.IsType<OkObjectResult>(result.Result);
            var persistedDto = Assert.IsType<MessageDataTransferObject>(okResult.Value);

            Assert.True(persistedDto.Id > 0, "Message ID should be auto-generated by the database.");
            Assert.Equal("Hello World!", persistedDto.Content);

            using var assertDb = dbFactory.CreateDbContext();
            var savedMessage = await assertDb.Messages.OfType<TextMessage>().FirstOrDefaultAsync(m => m.MessageId == persistedDto.Id);
            
            Assert.NotNull(savedMessage);
            Assert.Equal(conversation.ConversationId, savedMessage.ConversationId);
            Assert.Equal(sender.PamUserId, savedMessage.MessageSenderId);
            Assert.Equal("Hello World!", savedMessage.TextMessageContent);
        }

