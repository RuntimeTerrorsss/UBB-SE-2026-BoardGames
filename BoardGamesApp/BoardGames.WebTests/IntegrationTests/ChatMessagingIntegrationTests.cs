        [Fact]
        public async Task SendMessage_FromSuspendedUser_ReturnsForbidden()
        {
            // Arrange
            using var scope = _fixture.Factory.Services.CreateScope();
            var dbFactory = scope.ServiceProvider.GetRequiredService<IDbContextFactory<AppDbContext>>();
            using var db = dbFactory.CreateDbContext();

            // Create a new suspended user for this test
            var suspendedUser = new User 
            { 
                Id = Guid.NewGuid(), 
                PamUserId = 202, 
                Username = "suspended_user",
                DisplayName = "Suspended User", 
                Email = "suspended@test.com", 
                PasswordHash = "hash",
                Country = "TestCountry",
                City = "TestCity",
                IsSuspended = true
            };
            var receiver = await db.Users.FirstAsync(u => u.PamUserId == 201);

            db.Users.Add(suspendedUser);
            
            var conversation = new Conversation
            {
                Participants = new List<ConversationParticipant>
                {
                    new ConversationParticipant { UserId = suspendedUser.PamUserId },
                    new ConversationParticipant { UserId = receiver.PamUserId }
                }
            };
            db.Conversations.Add(conversation);
            await db.SaveChangesAsync();

            var (_, controller) = await CreateTestSubject(scope);

            var messageDto = new MessageDataTransferObject(
                0, conversation.ConversationId, suspendedUser.PamUserId, receiver.PamUserId, DateTime.UtcNow,
                "I shouldn't be able to send this", MessageType.MessageText, string.Empty, false, false, false, false, -1, -1, -1);

            // Act
            var result = await controller.SendMessage(messageDto);

            // Assert
            var objectResult = Assert.IsType<ObjectResult>(result.Result);
            Assert.Equal(403, objectResult.StatusCode);
            Assert.Contains("suspended", objectResult.Value?.ToString(), StringComparison.OrdinalIgnoreCase);
        }
